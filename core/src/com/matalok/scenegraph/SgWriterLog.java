//------------------------------------------------------------------------------
package com.matalok.scenegraph;

//------------------------------------------------------------------------------
import com.matalok.scenegraph.SgUtils.IPlatform;

//------------------------------------------------------------------------------
public class SgWriterLog 
  implements SgUtils.IWriter {
    //**************************************************************************
    // SgWriterLog
    //**************************************************************************
    private IPlatform m_platform;

    //--------------------------------------------------------------------------
    public SgWriterLog(IPlatform platform) {
        m_platform = platform;
    }

    //**************************************************************************
    // IWriter
    //**************************************************************************
    @Override public void Write(String str, Object... args) {
        m_platform.Inf(str, args);
    }

    //--------------------------------------------------------------------------
    @Override public void Flush() {
    }

    //**************************************************************************
    // Object
    //**************************************************************************
    @Override public String toString() {
        return "log";
    }
}
